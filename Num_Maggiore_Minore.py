#Scrivi un programma che chieda due numeri e mostri se il primo è maggiore o minore del secondo.

num1 = int(input("Inserisci un numero: "))
num2 = int(input("Inserisci un altro numero: "))

if num1 > num2:
    print("Il primo numero è maggiore del secondo:")
else:
    print("Il primo numero è minore del secondo")
